#define FONT_W   8
#define FONT_H   16
#define FONT_MIN 0x20
#define FONT_MAX 0x7e

/* ── text cursor ────────────────────────────────────────── */
static uint32_t cur_x  = 0;
static uint32_t cur_y  = 0;
static uint32_t col_fg = FB_WHITE;
static uint32_t col_bg = FB_BLACK;

void fb_set_fg(uint32_t rgb) { col_fg = rgb; }
void fb_set_bg(uint32_t rgb) { col_bg = rgb; }

uint32_t fb_get_fg(void) { return col_fg; }
uint32_t fb_get_bg(void) { return col_bg; }

/* ── init ───────────────────────────────────────────────── */
void fb_init(void)
{
    if (!fb_addr) return;
    fb_clear(col_bg);
    cur_x = 0;
    cur_y = 0;
}

/* ── draw one glyph ─────────────────────────────────────── */
static void draw_glyph(uint32_t px, uint32_t py, char c,
                        uint32_t fg, uint32_t bg)
{
    if (c < FONT_MIN || c > FONT_MAX) c = ' ';
    const uint8_t *glyph = font8x16[c - FONT_MIN];
    for (uint32_t row = 0; row < FONT_H; row++) {
        uint8_t bits = glyph[row];
        for (uint32_t col = 0; col < FONT_W; col++) {
            uint32_t color = (bits & (0x80 >> col)) ? fg : bg;
            fb_putpixel(px+col, py+row, color);
        }
    }
}

/* ── cursor: یه بلاک سفید زیر کاراکتر فعلی ─────────────── */
void fb_cursor_show(void)
{
    if (!fb_addr) return;
    /* ۲ پیکسل آخر از سلول رو رنگ می‌کنیم */
    fb_fill_rect(cur_x, cur_y + FONT_H - 2, FONT_W, 2, col_fg);
}

void fb_cursor_hide(void)
{
    if (!fb_addr) return;
    fb_fill_rect(cur_x, cur_y + FONT_H - 2, FONT_W, 2, col_bg);
}

/* ── scroll ─────────────────────────────────────────────── */
static void fb_scroll(void)
{
    uint8_t *base = (uint8_t*)fb_addr;
    uint32_t row_bytes = fb_w * (fb_bpp / 8);

    for (uint32_t y = 0; y + FONT_H < fb_h; y++)
        kmemcpy(base + y * fb_pitch,
                base + (y + FONT_H) * fb_pitch,
                row_bytes);

    for (uint32_t y = fb_h - FONT_H; y < fb_h; y++) {
        uint32_t *row = (uint32_t*)(base + y * fb_pitch);
        for (uint32_t x = 0; x < fb_w; x++)
            row[x] = col_bg;
    }

    cur_y = fb_h - FONT_H;
}

/* ── fb_putc ────────────────────────────────────────────── */
void fb_putc(char c)
{
    if (!fb_addr) return;

    if (c == '\n') {
        cur_x  = 0;
        cur_y += FONT_H;
        if (cur_y + FONT_H > fb_h) fb_scroll();
        return;
    }

    if (c == '\r') { cur_x = 0; return; }

    if (c == '\b') {
        if (cur_x >= FONT_W) cur_x -= FONT_W;
        draw_glyph(cur_x, cur_y, ' ', col_fg, col_bg);
        return;
    }

    draw_glyph(cur_x, cur_y, c, col_fg, col_bg);
    cur_x += FONT_W;

    if (cur_x + FONT_W > fb_w) {
        cur_x  = 0;
        cur_y += FONT_H;
        if (cur_y + FONT_H > fb_h) fb_scroll();
    }
}

void fb_puts(const char *s)
{
    while (*s) fb_putc(*s++);
}
