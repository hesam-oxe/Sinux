/* ── framebuffer state ──────────────────────────────────── */
static uint32_t *fb_addr  = 0;
static uint32_t  fb_pitch = 0;
static uint32_t  fb_w     = 0;
static uint32_t  fb_h     = 0;
static uint8_t   fb_bpp   = 0;

void fb_set_info(uint64_t addr, uint32_t pitch,
                 uint32_t width, uint32_t height, uint8_t bpp)
{
    fb_addr  = (uint32_t*)(uintptr_t)addr;
    fb_pitch = pitch;
    fb_w     = width;
    fb_h     = height;
    fb_bpp   = bpp;
}

int fb_available(void) { return fb_addr != 0; }

/* ── pixel / rect ───────────────────────────────────────── */
void fb_putpixel(uint32_t x, uint32_t y, uint32_t rgb)
{
    if (!fb_addr || x >= fb_w || y >= fb_h) return;
    uint32_t *row = (uint32_t*)((uint8_t*)fb_addr + y * fb_pitch);
    row[x] = rgb;
}

void fb_fill_rect(uint32_t x, uint32_t y,
                  uint32_t w, uint32_t h, uint32_t rgb)
{
    for (uint32_t dy = 0; dy < h; dy++)
        for (uint32_t dx = 0; dx < w; dx++)
            fb_putpixel(x+dx, y+dy, rgb);
}

void fb_clear(uint32_t rgb)
{
    fb_fill_rect(0, 0, fb_w, fb_h, rgb);
}

